/* matrix : given input matrix, you are require
 to change it in place without using extra space */
void rotate(vector<vector<int> >& mat) {
    // Your code goes here
    int row = mat.size();
    int col = mat[0].size();
    
    vector<vector<int>> ans(col, vector<int>(row));

    for(int i = 0; i < row; i++){
        for(int j = 0; j < col; j++){
            ans[j][row - i - 1] = mat[i][j];
        }
    }

    mat = ans;
}


